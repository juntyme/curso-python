"""
Operadores aritiméticos
+, -, *, /, //, **, %, ()

// - Divisão inteira o resultado é aredondado
** - Potenciação o número elevado a outro
% - Retorna o Modulo o resto da divisão entre os numeros
() - Retornar as procedencias
"""

print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração -', 10 -5)
print('Divisão /', 10 / 2)

print('Multiplicação *', 10 * '10') # Operador de Repetição
print('Adição', '5' + '5' )  # Faz a concatenção / Juntar valores

print(10 // 3) # Retorna um valor arredondado.
print(2 ** 10)  # Retorna a potencia
print(10 % 3) # realizando a divisão e recebendo o resto
print(5+2*10)
print((5+2)*10) # Alterar precedencia entre valores